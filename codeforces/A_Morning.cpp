void dbg_out() { cerr << endl; }
template <typename Head, typename... Tail> void dbg_out(Head H, Tail... T) {
  cerr << ' ' << H;
  dbg_out(T...);
}
#define dbg(...) cerr << "(" << #__VA_ARGS__ << "):", dbg_out(__VA_ARGS__)

void run_case() {
    string s;
    cin >> s;
    int cur = 1;
    int ans = 0;
    for (int i = 0; i < 4; i++) {
        int d = s[i] - '0';
        if (d == 0) d = 10;
        ans += 1 + abs(cur - d);
        cur = d;
    }

    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}
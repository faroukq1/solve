void dbg_out() { cerr << endl; }
template <typename Head, typename... Tail> void dbg_out(Head H, Tail... T) {
  cerr << ' ' << H;
  dbg_out(T...);
}
#define dbg(...) cerr << "(" << #__VA_ARGS__ << "):", dbg_out(__VA_ARGS__)

void run_case() {
    int N , M , ans = 0;
    cin >> N >> M;
    vector<string> words(N);
    for (auto &w : words)
        cin >> w;

    for (auto w : words) {
        const int len = (int)w.length();
        if (M - len < 0)
            break;

        M -= len;
        ans++;
    }

    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}
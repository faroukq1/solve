void dbg_out() { cerr << endl; }
template <typename Head, typename... Tail> void dbg_out(Head H, Tail... T) {
  cerr << ' ' << H;
  dbg_out(T...);
}
#define dbg(...) cerr << "(" << #__VA_ARGS__ << "):", dbg_out(__VA_ARGS__)

void run_case() {
    string s;
    cin >> s;
    bool appear_twice = false;
    const int N = s.length();
    for (int i = 0; i < N - 1; i++)
        if (s[i] == s[i + 1]) {
            appear_twice = true;
            break;
        }

    cout << (appear_twice ? 1 : N) << endl;
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}